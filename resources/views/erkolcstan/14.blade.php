<p>
    <em>
        - Minden Istentől van az üdvösségemért
    </em>
</p>
<p>
    A Teremtés könyvének 1. fejezetében olvassuk:
    <em>
        „Isten megteremtette az embert, saját képmására, az Isten képmására teremtette őt, férfinek és nőnek teremtette őket. Isten megáldotta őket, Isten szólt hozzájuk: ’Legyetek termékenyek, szaporodjatok, töltsétek be a földet és vonjátok uralmatok alá. Uralkodjatok a tenger halai, az ég madarai és minden állat fölött, amely a földön mozog." Azután ezt mondta Isten: "Nézzétek, nektek adok minden növényt az egész földön, amely magot terem, és minden fát, amely magot rejtő gyümölcsöt érlel, hogy táplálékotok legyen. A mező vadjainak, az ég madarainak s mindennek, ami a földön mozog és lélegzik, minden zöld növényt táplálékul adok.’ Úgy is történt. Isten látta, hogy nagyon jó mindaz, amit alkotott”
    </em>
    (27-31).
</p>
<ol>
    <li>
        A tulajdon a bűnbeesés előtt
        <ol>
            <li>
                „Nektek adok mindent”… A korlátlan bőség nemcsak annak köszönhető, hogy csak egy-két ember van, hanem hogy teljesen hozzáférnek Isten végtelen gazdagságához, az a bőség nem árt nekik, mert még nincs bennük rosszra való hajlam, nincs szükség „enyém-tiédre”, mert a közös öröm a cél, nincs önzés, a javak különbözősége szerinti kiszolgáltatottság vagy visszaélés a másikkal szemben.
            </li>
            <li>
                A teremtményekben az emberiség közös, továbbteremtő küldetése jelenti az élet értelmét, mert ezzel válaszolunk Isten szeretet-ajándékára.
            </li>
        </ol>
    </li>
</ol>
<ol start="2">
    <li>
        Mit eredményez a bűnbeesés?
        <ol>
            <li>
                A bűn által megsérült lelkű emberek között kialakult új alaphelyzetet nagyon jól szemlélteti a szemérem megjelenése, amikor fügefalevél-kötényekkel takarják el addig fedetlen testüket, mert mindez a bizalmatlanság, a másikkal szembeni kiszolgáltatottság érzésének jele.
            </li>
            <li>
                Az ember Istenen kívüli, új biztonság-érzetet keres, ezt szolgálja a saját titkok, a szemérem, a magán és intim szféra, a szellemi birtoklás, az anyagi birtoklás, a tekintélybirtoklás, a társas birtoklás, és maga az identitás is.
            </li>
            <li>
                A birtoklás tehát olyasvalami, mint a szemérem, a bűnbeesés óta nem lehet nélküle élni. Birtoklás nélküli életforma esetleg zárt, konkrét szabályok szerinti összetartó közösségben képzelhető el, amilyen pl. a szerzetesség.
            </li>
        </ol>
    </li>
</ol>
<ol start="3">
    <li>
        Mi most, a földi próbatételben a tulajdon értelme, mire vonatkozzék?
        <ol>
            <li>
                <strong>
                    Önmagam
                </strong>
                birtoklása, ami nem más, mint uralom önmagam felett az üdvösség céljai szerint (egyszerűbben:
                <strong>
                    önuralom
                </strong>
                ). Isten „tulajdona” vagyok, a tőle független önrendelkezéssel a sötétség hatalmának adom át magam.
            </li>
            <li>
                <strong>
                    Szellemi javak
                </strong>
                birtoklása, amely csak az anyagi szempontokból érthető meg:
            </li>
            <li>
                Az
                <strong>
                    anyagi javak
                </strong>
                szükségesek az emberhez méltó élethez, de nem gyűjtöm, tartogatom, fogyasztom azt, ami káromra szolgál, ami rongálja az egészségemet, visszataszítóvá tesz, gátol a fejlődésemben. Szellemileg éppígy, csak az a tudás vagy alkotói eredmény válik saját magam és az emberiség javára, amely az üdvösségkereső életet szolgálja.
                <em>
                    Az öncélúság olyan jelenség, mint amikor kövekkel megrakott hátizsákot cipelek. Ez hasznos lehet, mert erősödhetek tőle, de amikor rohannom kéne, hogy életet mentsek, akkor meg kell szabadulnom tőle, mert a magam edzésénél sokkal fontosabb szerepem adódott.
                </em>
            </li>
            <li>
                A
                <strong>
                    pénz
                </strong>
                szerepe az életben, hogy a szellemi és anyagi javak szabadabban, kötetlenebb lehetőségekkel szerezhessem meg. Ez a szélesebb látókörben feltáruló választék azonban nyilván veszélyes is, mert helytelen döntések és következményeik egész sorát is elénk tárják.
            </li>
            <li>
                Melyek tehát az
                <strong>
                    igazságosság
                </strong>
                feltételei a tulajdon terén? Az az enyém, amiért becsületesen megküzdöttem, amit ajándékba kaptam, vagy tisztességes szerencsével jutottam hozzá.
            </li>
            <li>
                A tulajdon tehát az
                <strong>
                    üdvösségkeresés
                </strong>
                eszköze: anyagi és szellemi javaim célja, hogy lelki fejlődésemet szolgálja, féken tartsa az önkénytől való kiszolgáltatottságomat, és jótékonyan enyhítsem vele mások kiszolgáltatottságát is.
            </li>
        </ol>
    </li>
    <li>
        Konkrét bűnök a tulajdon ellen
        <ol>
            <li>
                A
                <strong>
                    lopás
                </strong>
                a fent említett becsületes tulajdonszerzésen kívül minden, ami méltánytalanul, tisztességtelenül került a birtokomba, különösen, ha másokat megkárosítottam vele. A szellemi javak haszonszerző eltulajdonítása a plágium.
            </li>
            <li>
                A
                <strong>
                    csalás
                </strong>
                másoknak anyagi, erkölcsi, érzelmi vagy szellemi kárt okozó félrevezetése.
            </li>
            <li>
                A
                <strong>
                    zsarolás
                </strong>
                mások bajával való önkényes, haszonszerző visszaélés.
            </li>
            <li>
                A
                <strong>
                    rongálás
                </strong>
                vagy garázdaság önmagam vagy mások felelőtlen megkárosítása úgy, hogy ebből senkinek sem származik haszna, csak lázadón szembefordultam az ember továbbteremtő hivatásával.
            </li>
            <li>
                A
                <strong>
                    pazarlás
                </strong>
                az üdvösségemre kapott anyagi, szellemi erkölcsi javak, a tehetség és az idő önkényes eltékozlása azzal az illúzióval, hogy mindezek felett Istentől függetlenül rendelkezem.
            </li>
            <li>
                Mások megrövidítéséért csak úgy nyerhetek
                <strong>
                    bűnbocsánatot
                </strong>
                , hogy vissza is juttatok mindent, amivel kárt okoztam.
            </li>
        </ol>
    </li>
</ol>
