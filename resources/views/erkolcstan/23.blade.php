        <ol>
            <li>
                A családi kötelék, melyben Jézus megtestesült azokat jelenti (szülők, nagyszülők, közelebbi rokonok), akik Őt vállalták, elfogadták és akiket Ő maga vállalt. Ez a közösségi forma a szeretet kötelékének alapvető modellje.
            </li>
            <li>
                Emberi, baráti kapcsolatai azokból kerültek ki, akik természetesen közel álltak a lelkéhez és akikben megbízott.
            </li>
            <li>
                Sokan voltak, akik fontosak voltak a számára, mert rászorultak, akikben megmutathatta egyrészt a felebaráti szeretet példáját, másrészt a megváltásra szoruló ember és Isten kapcsolatát.
            </li>
            <li>
                Isteni szeretetének körébe tartoztak azok is, akikkel emberileg nem tudott mit kezdeni, mert elutasították Őt.
            </li>
        </ol>
    </li>
