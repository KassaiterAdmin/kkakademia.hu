<ol start="3">
    <li>
        Emberi kapcsolataim – Jézus életének mintája alapján
        <ol>
            <li>
                A család – a méltányos kapcsolatok helye – nem én választottam a körülöttem élőket, akik közé születtem, de Isten adta a természetes érzelmi kötelékeket is.
            </li>
            <li>
                A barátság az a szabadon választott és mégsem lefoglaló kapcsolat, melyet a bizalom és kölcsönös emberi vonzódás tart össze, ezáltal ez életem háttér-erőforrása.
            </li>
            <li>
                A szerelem a teljes embert, testével és lelkével a másikhoz vonzó kapcsolat, melynek alapvető jellege a „kiegészülés” – keresni a másikban azt a társat, akivel teljes, egész ember, „egy test” (Teremtés könyve 2,24) lehetek.
            </li>
            <li>
                Az igazi barátság és szerelem mércéje:
                <p>
                    TÖBBÉ TESZ VAGY KEVESEBBÉ TESZ?
                </p>
                <ul>
                    <li>
                        Többé tesz, ha úgy emel fel, hogy fiatalok életébe illő, de nemes, és a teljes embert érintő fejlődést szolgálja.
                    </li>
                    <li>
                        Vagy te emeled fel a másikat, mert erre valóban képes vagy és ez lelkileg felemelően hat rád is, vagy ő húz le téged (vagy fordítva).
                        <em>
                            Olyan ez, mintha egy meredek lejtőn valaki magával rántja a másikat is, aki aztán ettől még lejjebb sodorja őt.
                        </em>
                    </li>
                    <li>
                        A hívő közösségben sértő egymásra, a többi fiatalra nézve, ha üres, de szórakoztató kapcsolatokat választunk ki magunknak, holott egy ilyen közösség tagjai rendelkezésre állnának a barátsághoz.
                    </li>
                </ul>
            </li>
            <li>
                Ki a felebarát? – Jézus precízen válaszol az irgalmas szamaritánus esetével (Lukács evangéliuma 10,30-37). A bennünk – konkrét személyektől függetlenül élő - szeretet készsége Jézus kedvéért van jelen, amely a váratlan helyzetekre is nyitva áll az idegenekkel szemben is.
