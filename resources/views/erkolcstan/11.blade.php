    <li>
        Élethivatásunk keresése
    </li>
</ol>
<p>
    Amíg mindkét irányban lekötetlen vagy, addig minkét irányban nyitva áll Isten veled kapcsolatos akarata. Két szempont az élethivatás keresésére:
</p>
<ul>
    <li>
        Nem emberi logika alapján, hanem az Istennel fenntartott eleven kapcsolattal azt kérdezzem meg magamtól:
        <strong>
            mit vár Ő tőlem,
        </strong>
        és ne azt, hogy én mit várok az élettől és ehhez milyen segítségre számítok Tőle.
    </li>
    <li>
        Minden élethivatást
        <strong>
            át kell élnem
        </strong>
        bensőleg, gondolatban, ne legyenek számomra eleve kizárt lehetőségek!
    </li>
</ul>
