<p>
    János evangéliumának 14. fejezetében Jézus apostolaitól búcsúzik:
    <em>
        „Hisz ismeritek oda az utat, ahová megyek. Erre Tamás azt mondta: Uram, nem tudjuk, hova mégy, hát hogy ismerhetnénk az utat? Én vagyok az Út, az Igazság és az Élet - válaszolta Jézus. Senki sem juthat el az Atyához, csak általam”(
    </em>
    4-6).
</p>
<p>
    A 18. fejezetben pedig Pilátussal hangzik el a tömör, de tanulságos párbeszéd:
    <em>
        ”Arra születtem, s azért jöttem a világba, hogy tanúságot tegyek az igazságról. Aki az igazságból való, hallgat szavamra. Pilátus ezt mondta: Mi az igazság?"
    </em>
    (36-38).
</p>
<ol>
    <li>
        A keresztény ember igazságfogalma
        <ol>
            <li>
                Az Igazság – maga Jézus Krisztus, aki egyházának az Igazság Lelkét küldte el, hogy a saját életében megerősítsen bennünket. Ő maga fogalmazza meg az elavult hagyományoktól mentes, új, alapvetően az igaz életből fakadó gyakorlatot Máté evangéliumának 5. fejezetében:
                <em>
                    „Hallottátok ezt a régieknek szóló parancsot is: Ne esküdjél hamisan, hanem tartsd meg az Úrnak tett esküdet. Én pedig azt mondom nektek: Egyáltalán ne esküdjetek, sem az égre, mert az Isten trónja, sem a földre, mert az lába zsámolya, sem Jeruzsálemre, mert az a nagy Király városa. Még saját fejedre se esküdjél, mert egyetlen hajszáladat sem tudod fehérré vagy feketévé tenni. Így beszéljetek inkább: az igen igen, a nem nem. Ami ezen felül van, a gonosztól való”
                </em>
                (33-37).
            </li>
            <li>
                Az igazság fogalma egy piramishoz hasonlít. A csúcsköve határozza meg az összes alatta elhelyezkedő, belőle érvényt szerző részigazságot. Ez a csúcskő, a mindenséget és valóságot meghatározó, minden részigazságnak érvényt adó
                <strong>
                    alapigazság
                </strong>
                , hogy
                <strong>
                    <em>
                        Isten a mi Atyánk, aki Fiában, Jézusban nyilatkoztatta ki, hogy szeret hazavár bennünket
                    </em>
                </strong>
                . Ezt kaptuk meg Jézus Krisztustól a kinyilatkoztatás összefoglalásaként, mint alapvető igazságot. Minden más igazságnak tehát ebből kell kiindulnia. „Mert úgy szerette Isten a világot, hogy egyszülött Fiát adta oda, hogy aki hisz benne, az el ne vesszen, hanem örökké éljen”(János evangéliuma 3,16).
            </li>
            <li>
                A tények csak mozaikkockák, úgy variálható velük a valóság feltárása, hogy az igazsággal tökéletesen szembeállhat, ezért van szükségünk a kinyilatkoztatott alapigazságra.
            </li>
        </ol>
    </li>
</ol>
<ol start="2">
    <li>
        Az igazság képviselete
        <ol>
            <li>
                Az alapigazság tehát, hogy Isten a szeretet, tehát a mi igazságunk alapja is a szeretet.
            </li>
            <li>
                A szeretet nem egyszerű érzelem, hanem az Isten szeretetének megvalósítására vonatkozó készség. Az igazság képviselésének első vonása: a személy, a krisztusi ember igaz-volta.
            </li>
            <li>
                Nem fér össze az egyéniségemmel az igaztalanság (hazugság, csalás, képmutatás, félrevezetés, a magam igazának – valósággal ellentétes erőszakolása), mert benső világom Isten objektivitására épül.
            </li>
            <li>
                A rágalmazás is az igaztalanság bűne, egyszerre lopás és hazugság. Valakire valami bűnt fogok rá, ezzel megsértem, ellopom a becsületét. Olyan mértékű bűn, amilyen mértékű bűnt az illetőre ráfogok, vagy amilyen nagyságú kárt okozok neki, akár jelentéktelen hazugsággal is. Ahogy az ellopott javakat vissza kell szolgáltatni a bűnbocsánathoz, ugyanígy az ellopott becsületet is, vagyis vissza kell vonni azok előtt, akik előtt elhangzott. Ennek a bűnnek akkor is részese vagyok, ha gyanútlanul ugyan, de ellenőrizetlenül továbbadok egy rágalmat.
            </li>
            <li>
                Az őszinteség erény, amely az igazság helyes, mértékletes, tapintatos és diszkrét képviselete a szeretet-kapcsolatokban (nem őszinteség az indiszkréció és a másoknak terhes őszinteségi roham).
            </li>
            <li>
                A korrektség a méltányos igazságosság megtartása akkor is, ha ez nem az én érdekeimet szolgálja, hiszen hosszú távon mindenkinek hasznos.
            </li>
        </ol>
    </li>
</ol>
<p>
    Beszéljünk a becsülettel kapcsolatos bűnökről!
</p>
<ol>
    <li>
        Mi a hazugság? El tudod-e képzelni olyan formáját, amellyel nem vétkezel az igazság ellen?
    </li>
    <li>
        El tudsz-e képzelni olyan igazmondást, amellyel vétkezel a szeretet ellen?
    </li>
    <li>
        Csalás-e az adócsalás, a kereskedelmi őszintétlenség, önmagunk jellem vagy képességbeli hiányosságainak letagadása egy állásinterjú során?
    </li>
    <li>
        Az élet mely területein várja el tőlünk Jézus az őszinteséget?
    </li>
</ol>