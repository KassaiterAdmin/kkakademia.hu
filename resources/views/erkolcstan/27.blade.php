<p>
    <strong>
        Személyes feltámadásom reménye Jézusban
    </strong>
</p>
<p>
    Szembesülök a halál utáni élet távlataival.
</p>
<p>
    <em>
        Egy gömbben együtt él egy hernyó és pillangó. A hernyó csak síkban mozog és így könnyedén kijelenti, hogy ez a gömb a végtelenég,mert ő itt sehol nem ütközik akadályba. A pillangó röpül, és minduntalan a gömb falának csapódik, ami fájdalmat okoz neki. Ezért kijelenti: kell lennie valaminek, folytatódnia kell a valóságnak a gömbön kívül is, különben nincs értelme annak az erőnek és képességnek, amivel ő tovább tudna repülni.
    </em>
</p>
<p>
    Igazi, nagy szellemek, nemes lelkű géniuszok nem veszhetnek el csak azért, mert testükben leállt egy életfontosságú szerv. Az ember végtelen életre vonatkozó vágya továbbélési képességére utal. Ez jóval több mint a biológiai életösztön, hiszen nem feltétlenül az élet meghosszabbítására, hanem örök voltának kibontakozására vonatkozik. Nem elégszem meg a lélek életével, amikor teljes személyiségként, test-lélek egységében élem meg önmagam, ehhez az egységhez kötődnek az élményeim, személyi érettségem. A múlandóság fáj, a remény éppen nem a beletörődés álbölcsessége.
</p>
<ol>
    <li>
        <strong>
            Jézus feltámadása – a megváltás értelme
        </strong>
        <ol>
            <li>
                A feltámadás egyetemes válasz az emberi élet megoldatlanságaira, amilyenek a bűntudat, a szenvedés, az ellentétek, a különféle sorskérdések, a halál vagy a múlandóság.
            </li>
            <li>
                Ezt az örömhírt aszerint kell elfogadnom, hogy nem evidencia, nem valamiféle felvilágosult, általános nézetrendszer, hanem próbára teszi a hitemet, szembefordít a fantáziátlan tömeggel, de mindenképpen az igazi, teljes boldogságot jelenti, nemcsak valami pótmegoldást a múlandóságtól való félelem ellen.
            </li>
            <li>
                Jézus feltámadása után kizárólag övéinek jelenik meg. A kívülállók csak a hívők meggyőződésén keresztül értesülhetnek róla. Nemcsak elfogadnom kell: tanúságtevő vagyok, az átélés kötelez a feltámadott élet továbbadására.
            </li>
        </ol>
    </li>
</ol>
<ol start="2">
    <li>
        <strong>
            A liturgia átélése
        </strong>
        <ol>
            <li>
                A szombat csendje tele van várakozással. Önmagamat keresem, alapvető kérdéseimet teszem fel: ki vagyok egyáltalán Isten szemében, mit kell kezdenem az életemmel, mit ad hozzá az életemhez Jézus feltámadása, mivé kell válnom az egyház számára?
            </li>
            <li>
                A tűz megszentelése a világ újrateremtésének jelképe, Isten ismét szól: „Legyen világosság”! A megszentelt láng a sötétet, a sátán birodalmát töri meg – bennem is.
            </li>
            <li>
                Krisztus világossága – az egyetlen világosságom, örökérvényű fény, leszámolás az álfényekkel, hamis életértelmezésekkel.
            </li>
            <li>
                A Húsvét Örömének (Exultet) – a felszabadult, tiszta öröm átélése, hálaadás mindazért, ami életemben Krisztushoz köt, a feltámadás befogadása.
            </li>
            <li>
                Az igeliturgia: az ószövetségi történetek megértésével együtt saját ószövetségem, próbatételeim, bűneim és felállásaim értelmét is megértem.
            </li>
            <li>
                Keresztségi fogadalmaim megújítása: a lelkemen lévő, eltörölhetetlen jel élővé tétele, keresztségem élményének felszítása.
            </li>
            <li>
                A körmenet: testvéreimmel tanúskodom mindarról, amit a Szentlélek segített átélnem, örömmel ajándékozom meg a világot.
            </li>
        </ol>
    </li>
</ol>
<ol start="3">
    <li>
        <strong>
            Húsvét ünnepe – a húsvéti ember
        </strong>
        <ul>
            <li>
                Az üresen talált sír felvetése: űr tátong bűneim helyén? Ha igen, akkor ezt valami felemelővel kell betöltenem.
            </li>
            <li>
                „Miért keresitek a holtak között az élőt?” – halljuk Jézus sírjánál az angyaltól. Miért keresem önmagam régi, sötét, megszokott kategóriáim között?
            </li>
            <li>
                Ugyanaz az ember maradtam, de mégsem ugyanolyan.
                <p>
                    <em>
                        Egy hittankönyv szemléltető ábráján két ember ugyanazt a munkát végzi: mindketten a tűző napon szüretelnek. Egyikük káromkodik a nehéz munka terhe alatt, zúgolódik a meleg miatt, este rosszkedvűen hárítja el családja szeretetét, hiszen értük szenvedi el mindezt. A másik ugyanezeket a nehézségeket viseli, de örül, hogy van munkája és bár tűzi a nap, elégedett, hogy nem esik az eső, nincs akadályoztatva. Hazatérve felüdíti őt családjának  szeretete, hiszen most látja maga előtt mindannak értelmét, amit nap közben művelt.
                    </em>
                </p>
                <p>
                    <em>
                        Nagy Sándor makedón uralkodó egy győztes, ám rendkívül nagy veszteségű, csatája után kijelenti: én vagyok a világ ura, a leghatalmasabb! Mire egyik hadvezére felveti: Sándor, ha valóban ilyen hatalmas vagy, támassz fel akár csak egyet is az elesett, remek makedón ifjak közül!
                    </em>
                </p>
                <p>
                    Úgy készüljek a Nagyhétre és a Húsvétra, hogy valóban nyomot hagyjon, átformáló erő legyen bennem a liturgia.
                </p>
            </li>
        </ul>
    </li>
</ol>
<p>
    <strong>
        Kérdések:
    </strong>
</p>
<ol>
    <li>
        Kapsz egy nagyon jó diákmunka ajánlatot: két aranyos, 4 éves ikergyerekre kell vigyáznod. A család örül, hogy hívő vagy, a szülők kérik is, hogy néha beszélj a Bibliáról, a benne szereplő történetekről a gyerekeknek, tanítsd őket imádkozni, de úgy, hogy ők reformátusok, úgyhogy se keresztvetés, se Mária és a szentek tisztelete, se szentmise, se katolikus templom rajzolgatása ne legyen benne. Elfogadod-e ezt a lehetőséget így, vagy nem, vagy hogyan?
    </li>
    <li>
        Beleszeretsz egy nálad egy-két évvel idősebb személybe. A kapcsolatotok tiszta és kölcsönös, úgy érzed, ő az, akit egy életre vállalni tudnál. Már tervezgettek is, amikor elmondja, hogy sajnos van egy hajdan, nagykorúsága hajnalán romantikus gyorsasággal megkötött házassága, melyből polgárilag elvált ugyan, de egyházilag még érvényben van. Mihez kezdesz ezzel a hírrel?
    </li>
</ol>

