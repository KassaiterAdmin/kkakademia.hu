<p>
    A krisztusi élet – konkrét megnyilvánulásokból áll: már Jézus életében is ezt jelzik a csodák, a böjt, a tanítás, az egyház kereteinek megteremtése, az apostolok felhatalmazása. Jézus életébe a szentségek által, a kegyelem erejével és a tanítást tudatosító hatásával kapcsolódunk be.
</p>
<ol>
    <li>
        <strong>
            A szentségek
        </strong>
        <ol>
            <li>
                <strong>
                    Jézus Krisztus által rendelt
                </strong>
                (vagy az evangéliumban leírtak alapján, vagy ugyan le nem írt módon, mégis az apostolok tetteiből Jézusra visszavezethetően),
            </li>
            <li>
                <strong>
                    érzékelhető
                </strong>
                (tehát fizikailag meghatározott)
                <strong>
                    és hatékony
                </strong>
                (tehát konkrét kegyelmi hatással rendelkező)
            </li>
            <li>
                <strong>
                    jelek
                </strong>
                (amelyek önmagukban is külön tanítást, szemléltetést hordoznak),
            </li>
            <li>
                amelyek
                <strong>
                    természetfeletti
                </strong>
                (tehát a fizikai érzékelés felett álló),
            </li>
            <li>
                <strong>
                    kegyelmet
                </strong>
                ( Isten lelki ajándékát, amely megszentel vagy segít minket)
            </li>
            <li>
                <strong>
                    közvetítenek
                </strong>
                (tehát önmaguknál a jelek megvalósulásánál és a kiszolgáltatás és felvétel szándékánál, valamint az érvényességi feltételeknél fogva).
            </li>
        </ol>
    </li>
</ol>
<p>
    <em>
        ( A vastag betűs szavak összeolvasásával a szentségek egyszerű meghatározásához jutunk, amely a fentinél kevésbé részletezően is magyarázható.)
    </em>
</p>
<p>
     
</p>
<ol start="2">
    <li>
        <strong>
            Gyónásra készülök
        </strong>
        <ol>
            <li>
                Ez mindig megtérés: most az életem kifordul a sarkaiból és minden megújul, hiszek a belső gyógyulás csodában!
            </li>
            <li>
                Nyugodt alkalmat termetek a felkészülésre, ennek kezdete a Szentlélek segítségül hívása és a
                <strong>
                    lelkiismeretvizsgálat
                </strong>
                :
            </li>
            <li>
                A gyónás nem esszészerű vallomások sorozata, hanem
                <strong>
                    őszinte és tárgyilagos szembenézés
                </strong>
                belső életemmel. Alapvető kategóriák:
<p>
    <strong>
        Bűn:
    </strong>
    Tudva és akarva nem engedelmeskedem Isten akaratának.
</p>
<p>
    <strong>
        Mivel?
    </strong>
    Gondolattal, szóval, cselekedettel és mulasztással.
</p>
<p>
    <strong>
        Hogyan? Súlyos bűn,
    </strong>
    amely kioltja a lelkemből a megszentelő kegyelmet, az Istennel való kiengesztelődést, ha valami jelentős ellenszegülést akarattal teszek, vagy mulasztással valósítok meg. Ezeket mindenképpen egytől egyig meg kell gyónnom, gyakoriságukkal együtt, hogy bocsánatot nyerjek és áldozhassak.
</p>
<p>
                  
    <strong>
        Bocsánatos bűn,
    </strong>
    amelyből vagy a nagyobb jelentőség, vagy a tudatosság hiányzik. Ezektől az őszinte bűnbánat is megszabadít, de legalább havonta ezekre is helyes feloldozást és lelki vezetést kérni.
</p>
<p>
    <strong>
        Mi a célom? Az örök üdvösségre készülök,
    </strong>
    Isten irgalmát minél teljesebben kell tudnom befogadni, eszerint kell alakulnom. Minden jóra Isten kegyelméből vagyunk képesek, de erőfeszítést jelent ebben elfogadása is, ez az
    <strong>
        érdem
    </strong>
    . Ha rendszeres és tudatos törekvés által megvalósítok egy bizonyos jóra, érdemre való készséget életemben, szintén a kegyelem segítségével, azt
    <strong>
        erénynek
    </strong>
    hívjuk. A gyónás tehát nemcsak megtisztulást, de lelki továbblépést is kell, hogy eredményezzen.
</p>
<p>
    Ezek ismeretében kérdezzem meg magamtól:
</p>
<ul>
    <li>
        Boldog ember vagyok-e, úgy, ahogy Jézus akarta, amiért megváltott?
    </li>
    <li>
        Mi tart vissza a felhőtlen, Istenben való örömtől?
    </li>
    <li>
        Mi az, ami különösebb gondolkodás nélkül is bánt, ami először eszembe jut, amit visszafordítanék, ha tehetném, mert nagyon helytelenül tettem, vagy ha rosszul sült el egy esemény, mi az én felelősségem benne?
    </li>
    <li>
        Mi az, amiben ezen kívül vétkeztem, érzelmileg nem bánt ugyan, de kezd megszokottá és ettől egyre veszélyesebbé válni?
    </li>
    <li>
        A legveszélyesebb bűneim azok, amelyeket nem tartok annak, csak az egyház, vagy a lelki vezető ajánlja figyelmembe, hogy kerüljem őket. Melyek ezek?
    </li>
</ul>
            </li>
    <li>
        <strong>
            A gyónás
        </strong>
        közelebb visz a
        <strong>
            Jézussal való párbeszéd
        </strong>
        hez. Elmondom a gyóntatón keresztül, milyen lelki korszakomban vagyok, milyen kegyelem ért, minek örülök, milyen gondok nyomasztanak, mire kérek tanácsot, milyen döntések előtt állok?
    </li>
    <li>
        <strong>
            Az elégtétel
        </strong>
        csak figyelmeztetés: szívből vegyek részt Jézus jóvátételében, és határozzam el erősen, hogy ismét nekifutok a szent életnek!
    </li>
        </ol>
</ol>
<ol start="3">
    <li>
        <strong>
            Áldozásra készülök
        </strong>
        <ol>
            <li>
                Egy nagy
                <strong>
                    ünnep
                </strong>
                , egy kivételes esemény előtt állok, ami lázban tart (fejesugrás egy veszélytelen, tiszta öbölbe, találkozás egy tiszta és jövőbe mutató szerelmi kapcsolatban, díjátadás egy megküzdött sikeremért – mennyi ünnep!), és a hét minden egyes napja felkészít a Jézussal való legbensőségesebb találkozásra, egyúttal felkészít az örök ünnepre is. Soha, senki nem állt ilyen közel hozzám.
            </li>
            <li>
                <strong>
                    Minden vasárnap
                </strong>
                törekedjek az áldozásra, ha tehetem, hétköznap is jussak el szentmisére!
                <em>
                    „Aki unalomból vagy kényelmességből nem gyónik és nem áldozik, azt kár volt megváltani!”
                </em>
                Miért is? Mert tisztában van ugyan a jelentősségével, de nem annak ragja szerint gondolkodik a szentségekről.
            </li>
        </ol>
    </li>
</ol>
<p>
    Legyen komoly a hálaadásod, és térj vissza az áldozás emlékére még napokig!
</p>
<p>
     
</p>
<ol start="4">
    <li>
        Naponkénti készület a
        <strong>
            házasság
        </strong>
        ra
        <ol>
            <li>
                Már most jegyes vagyok! Vagy az Istennek közvetlenül elkötelezetten, vagy egy fiúnak/lánynak, akit még nem ismerek, de a leendő rám bízottaknak: gyermekeimnek, unokáimnak mindenképpen.
            </li>
            <li>
                Úgy tudok helyesen dönteni a
                <strong>
                    lelki hivatás
                </strong>
                (papság, szerzetesség) mellett, ha bensőleg átéltem már a férj vagy feleség, apa vagy anya szerepét, és tetszik is, lelkesít is, csak esetleg
                <em>
                    valami ennél is nagyobb
                </em>
                van bennem.
            </li>
            <li>
                Ha viszont majd megházasodok, akkor már valahol él az a valaki, akivel ma már bensőleg jegyes vagyok, akkor is, ha még nem is ismerem. Csak tisztán gondolkodhatok róla, és mindenki másról. Napi imám:
                <ul>
                    <li>
                        <strong>
                            <em>
                                Uram, ha a családfői hivatást szántad nekem, akkor valahol már ott él az a valaki, akit nekem szántál, hogy egy életen át egymás Hozzád vezető társai legyünk a házasság szentségében. Add, hogy ismerve vagy ismeretlenül is hűséges legyek hozzá és a megfelelő időben megtaláljam őt!
                            </em>
                        </strong>
                    </li>
                    <li>
                        Majd szólítsam meg őt képzeletben, magamban! –
                        <strong>
                            <em>
                                Kedves leendő Házastársam! A mai napomon érted és majdani családunkért gazdagodom lélekben, szellemileg, érted élem át az élményeket, kalandokat, érted gyűjtöm tapasztalataimat, érted erősödök érzelmi érettségemben, érted és leendő gyermekeimért vigyázok az egészségemre. Ismeretlenül is szeretlek és tisztán őrzöm magam neked.
                            </em>
                        </strong>
                    </li>
                </ul>
            </li>
        </ol>
    </li>
</ol>
<p>
     
</p>
<ol start="5">
    <li>
        <strong>
            A betegek kenete
        </strong>
        <ol>
            <li>
                Igyekezzek a környezetemben élő betegeket meggyőzni a szentségek fontosságáról és papról gondoskodni! Készítsem fel rá: ez lelki megerősítés, kár lenne kihagyni Jézus gondoskodását, a szentségben rejlő kegyelmet, amikor igazán szükségünk van rá.
            </li>
            <li>
                Tisztázzam a szentkenet kettős hatását: vagy felmentést ad a további szenvedésektől, vagy megerősít és javulást hoz, hogy küldetésünket maradéktalanul be tudjuk tölteni.
            </li>
        </ol>
    </li>
</ol>
<p>
     
</p>
<p>
    Kérdések:
</p>
<p>
    Osszuk meg egymással az Oltáriszentséggel kapcsolatos legjelentősebb emlékeinket, élményeiket, akár valami liturgikus eseményről, akár valami nagyon bensőséges átélésről van szó.
</p>
<p>
    Mit válaszolsz annak az embernek, aki azt mondja: Azért nem gyónok, mert nekem felesleges ehhez egy kívülálló, én közvetlenül Istennel is el tudom intézni a lelkem mélyén a kiengesztelődést.
</p>