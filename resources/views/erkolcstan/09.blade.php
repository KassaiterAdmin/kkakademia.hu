                <em>
                    később ezekről még részletesebben is szó esik, most csak fussunk át egy előzetes összefoglalást
                </em>
                ): gondolattal (belső bűnök, titkos, tudatos átélések, melyeket tettekkel nem merünk megvalósítani, vagy nincs rájuk alkalmunk), szóval (minden lazítás, ízléstelenség, csábítás vagy felbujtás, a nemiség szent voltának lejáratása), cselekedettel (erkölcsi motiváció nélküli, öncélú tettek, nézés, bűnre vezető alkalom elfogadása), mulasztással (az aktívan ébren tartott lelki készenlétnek, elővigyázatosságnak, határok kölcsönös betartásának feladása).
            </li>
            <li>
                Nem az ártatlanság a cél (az fakadhat tehetetlenségből vagy mujaságból is), hanem a nemiséggel való tudatos engedelmesség. Ehhez persze az ártatlanság, vagy a megtérés nagyon fontos eszköz.
            </li>
        </ol>
    </li>
</ol>
<ol start="3">
    <li>
        Közfelfogás és kereszténység
        <ol>
            <li>
                Képzeld el, hogy holnap leszállnak az ufók és elmondják, hogy minden, amiben hittünk, csak illúzió volt. Hogyan alakulna tovább az életed? Ha simán, akkor a hited most sem sokat ér. Ha viszont úgy érzed, hogy ebbe beleroppannál, akkor már most is úgy tűnik – egy lapra teszel fel, amit csak tudsz. Helyes, mert az ufó-krízistől is ez mentene meg. Akkor viszont most se légy felemás keresztény: ezt elfogadom, ezt nem, a megváltás kell, az erkölcsi elvek nem - felfogással. Szokásos példák: az alkalmankénti túlbizalmaskodás, a társamnál való ott-alvás, esetleg vele való összeköltözés, a szentségi házasságban történő válás, a különféle okokra hivatkozó polgári házasság, az élettársi viszony… stb.). Ezektől való tartózkodásodat jobb alapkőként már most letenni az életedben.
            </li>
        </ol>
    </li>
</ol>
<p>
    Kérdések:
</p>
<ol>
    <li>
        Miben térhetnek el egy lány szexuális közeledési szokásai egy fiúétól?
    </li>
    <li>
        Mi az, amit ebből elfogadsz és mi az, amit a személlyel együtt utasítasz el, mondván, ha ő ilyen, akkor a közeledése sem kell.
    </li>
    <li>
        Táncos szórakozóhelyen megismerkedsz egy fiúval/lánnyal. Ő kezdeményez, elég egyértelműen közeledik. Mi az a határ, amit még elfogadsz, és mi az, amit nem? Mivel utasítod el? Hogyan kerül szóba, hogy azért nem bonyolódsz túlzásokba, mert hívő vagy? Mi van akkor, ha azt mondja rá, hogy sebaj, ő is?
    </li>
    <li>
        Mi a különbség egy lazább erkölcsű fiú és lány között?
    </li>
    <li>
        Szeretsz egy lányt/fiút, úgy érzed, őt szánta neked Isten. Egyértelműen vállalni akarod egy életre. Mit teszel, amikor bevallja, hogy van már egy előző, elhamarkodottan megkötött egyházi házassága?
    </li>
</ol>