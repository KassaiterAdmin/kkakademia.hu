<ol>
    <li>
        Az élet és az emberi élet alapjellege
    </li>
</ol>
<p>
    <a href="http://www.kereszteny.hu/biblia/showchapter.php?reftrans=1&amp;abbook=Ter&amp;numch=2#7">
        A Teremtés könyve 2. fejezetének 7.
    </a>
    versében olvassuk: „
    <em>
        Akkor az Úristen megalkotta az embert a föld porából és orrába lehelte az élet leheletét. Így lett az ember élőlénnyé”.
    </em>
    Isten aktuális akarata szerinti mozzanat, a háttérben felállhat akármilyen egyedfejlődési elmélet, minden aszerint és azzal a céllal alakult, hogy az ember megjelenjen olyan élőlényként, amelyik alkalmas a természetfeletti élet befogadására.
</p>
<p>
    Közvetlenül Isten ad életet a saját életéből. A lélegzet – lélegzés az élet jele. Isten saját élete – lelki élet. Ez az emberi élet alapjellege, elsődleges szempontja. Ugyanez a lény lesz, aki természete szerint majd Istent, Önmagát is képes lesz befogadni Jézus megtestesülésével.
</p>
<ol start="2">
    <li>
        Miért jobb élni, mint nem élni – a sors „szerencsésségétől” függetlenül?
    </li>
</ol>
<p>
    Isten élete a Szentháromságban valósul meg: Isten boldog, a szeretet örömé teljesedik be az életében, ennek átadásáért teremt
</p>
<p>
    János evangéliuma 1,4: „
    <em>
        Benne az élet volt, s az élet volt az emberek világossága”
    </em>
    – azaz az egyetlen lehetséges világosság, az élet igazi és kizárólagos értelme
    <em>
        .
    </em>
    Ez Isten benső, tiszta, hiteles szeretet-élete és az ebből fakadó boldogság.
</p>
<p>
    12-13. vers: „
    <em>
        Ám akik befogadták, azoknak hatalmat adott, hogy Isten gyermekei legyenek. Azoknak, akik hisznek nevében, akik nem a vérnek vagy a testnek a vágyából s nem is a férfi akaratából, hanem Istentől születtek”.
    </em>
    – A lét, az élet bűnbeesés utáni elfogadása a földi életminőségtől függetlenül a felkínált végső örömért történik. A vértanúságot (bűn elutasítását) kivéve az életnek minden körülmények közt elsősége van. Életünk a bűnbeesés után sem kap más értelmet, csak ezt így nehezebb felfedezni és ébren tartani a gondolkodásunkban.
</p>
<ol start="3">
    <li>
        <a href="http://www.kereszteny.hu/biblia/showchapter.php?reftrans=1&amp;abbook=Jn&amp;numch=3#16">
            János evangéliuma 3,16
        </a>
        :
        <strong>
            <em>
                „Mert úgy szerette Isten a világot, hogy egyszülött Fiát adta oda, hogy aki hisz benne, az el ne vesszen, hanem örökké éljen”.
            </em>
        </strong>
    </li>
</ol>
<p>
    Krisztus elfogadása határozottan azt jelenti, hogy a földi életemet az örök élet szempontjai határozzák meg.
</p>
<ul>
    <li>
        A földies esélyek mérlegelése során
        <a href="http://www.kereszteny.hu/biblia/showchapter.php?reftrans=1&amp;abbook=Mt&amp;numch=16#26">
            Máté evangéliumában, a 16,26
        </a>
        -ban olvassuk Jézus kérdését: „
        <em>
            Mi haszna van az embernek, ha az egész világot megszerzi is, de lelke kárát vallja? Mit is adhatna az ember cserébe a lelkéért?”.
        </em>
    </li>
    <li>
        Erőbefektetésünk mértéke, iránya az ösztönös küzdelmekben mindig megfontolást érdemel.
    </li>
    <li>
        <strong>
            Küldetésszerű életet formálunk!
        </strong>
        Legyen miért élni, legyek a helyemen, legyek áldozat! Ez az életem értéke.
    </li>
</ul>
<p>
    Lelkesedésem ne a halálra, hanem az örök életre vonatkozzon, a jó értelemben vett, cselekvő félelem az örök kárhozattól.
    <a href="http://www.kereszteny.hu/biblia/showchapter.php?reftrans=1&amp;abbook=Mt&amp;numch=13#42">
        Máté evangéliuma, 13,42
    </a>
    : „
    <em>
        Ezeket tüzes kemencébe vetik, ott sírás és fogcsikorgatás lesz”.
    </em>
</p>
