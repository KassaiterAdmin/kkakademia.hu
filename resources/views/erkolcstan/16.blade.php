            </li>
            <li>
                Semmi jóslás sincs abban, ha felmérjük, kivel tudnánk boldogan, küldetés-szerűen leélni az életünket. Praktikus tanács: húsz évesen ötvenéves korunkra gondoljunk! Mit tudunk majd akkor kezdeni egymással?
