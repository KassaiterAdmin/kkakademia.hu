<ol start="3">
    <li>
        Az eskü: Isten az életünkben
        <ol>
            <li>
                Jézus mondja:
                <em>
                    „Ne esküdjetek…legyen az igen - igen, a nem - nem!”
                </em>
                (Máté evangéliuma 5,34-37). Az eskü Isten bizonyságul hívása, olyan esetekben, amikor hisszük, hogy a Tőle kapott küldetésre vállalkozunk életfordulóinkon – tehát Jézus nem magát az esküt vagy fogadalmat, hanem Isten nevének jelentéktelen bizonyságul hívását tiltja, amely helyettesíteni akarja az alapvető igazmondást.
            </li>
            <li>
                Az eskü bennünket, magunkat határoz meg, ha megszegjük, elsősorban bennünk okoz törést, jellegtelen emberré válunk.
            </li>
            <li>
                Ennek megfelelően: Konfliktusok és válságok feloldásánál súlyos kínlódást okoz egy polgári házasságban (amely együttélési formát egyébként nem is tartjuk hitelesnek), annak felvetése, hogy mi is legyen velünk? Váljunk, vagy még együtt maradjunk? Az egyházi házasságban élők felmentést élveznek ez alól a kínos töprengés alól, hiszen számukra egyetlen kérdés adódhat: „Hogy hozzuk helyre?”.
            </li>
            <li>
                Most, kora ifjúságodtól építsd magadban a hűség erényét, apró önfegyelmezéseken, alkalmazkodásokon, komoly elmélkedéseken keresztül és a testi tisztaság tudatos őrzésével.
            </li>
            <li>
                A tisztaság a mentalitással kezdődik. Jézus többször mondja: „Mondatott a régieknek…, én pedig mondom nektek…”. Ma ezt üzeni az egyház: „Mondják a többiek…, Jézus pedig mondja nekünk”. 
            </li>
        </ol>
    </li>
</ol>
<ol start="4">
    <li>
        Ki az a harmadik?
        <ol>
            <li>
                A parancsolat: „Felebarátod házastársát ne kívánd!” – a házasságon kívülállóra vonatkozik. Ő egyszerre szeret és gyűlöl: szenvedélyesen ragaszkodik valakihez, akinek élete hátterében tönkreteszi a rábízottakat. Ezért ragaszkodása sem nevezhető szeretetnek. Leplezetten gyűlöli azt is, akit magának akar megszerezni, mert megtöri benne az esküt, ami tehát magát az embert határozza meg.
            </li>
            <li>
                Sose legyek én a harmadik! Akkor sem, ha az elkötelezett személy csábít! Szenvedélyesen esetleg, de hitelesen sosem fogom tudni őt szeretni.
            </li>
        </ol>
    </li>
</ol>
