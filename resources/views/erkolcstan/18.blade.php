<p>
    Mit kínál fel Krisztus? Mit kérek belőle? Mit méltó kérnem? Az élet mely területére terjed ki az Evangélium? Vannak-e vakfoltok?
</p>
<ol>
    <li>
        Jézus Krisztus teljes emberi élete
        <ol>
            <li>
                A krisztusi élet formálása terén nem az a kérdés, hogy Jézus megtartotta-e a közlekedési szabályokat, nézett-e tévét és hogyan szelektált a műsorok között, hanem hogy
                <strong>
                    milyen lelkülettel élt
                </strong>
                , korának összes életlehetőségét lefedve, és ezt hogyan hozzuk át a saját korunkba, egyéni körülményeink közé.
            </li>
            <li>
                Őbenne két természet volt – az emberi akaratnak kellett meghódolnia az isteni akarat előtt. Bennünk jelen van a hit: az emberi értelemnek, aztán akaratnak kell befogadnia a kinyilatkoztatásban Isten akaratát.
            </li>
            <li>
                Ha a titok mélyére akarunk hatolni, megkérdezzük, mi volt Jézus igazi lételeme? Válaszként János evangéliumának 4. fejezetében ezt olvassuk:
                <em>
                    „Közben a tanítványok kérték: Mester, egyél! De ő azt felelte:Van eledelem, csak nem tudtok róla. A tanítványok erre tanakodni kezdtek egymás közt: Csak nem hozott neki valaki enni? Jézus erre megmagyarázta: Az én eledelem, hogy annak akaratát teljesítsem, aki küldött, s elvégezzem, amit rám bízott”
                </em>
                (31-34). Nem egy parancsrendszer a döntő, hanem az isteni akaraton keresztül Isten megközelítése, az istengyermeki, krisztusi élet.
            </li>
        </ol>
    </li>
</ol>
<ol start="2">
    <li>
        A krisztusi élet az egyházban realizálható
        <ol>
            <li>
                Jézus tudatosítja bennünk saját életét, ezért ezt
                <strong>
                    tanításának hiteles értelmezésén
                </strong>
                keresztül tehetjük magunkévá.
            </li>
            <li>
                A
                <strong>
                    tanítás hiteles életre váltásán
                </strong>
                keresztül engedjük be Jézus gondolkodásmódját, életstílusát, érzületét, áldozatkészségét saját gondolatainkba, tetteinkbe.
            </li>
            <li>
                Az életre váltáshoz kapott erő segítségével, amely számunkra az általa rendelt
                <strong>
                    szentségek
                </strong>
                ben elérhető.
            </li>
            <li>
                Ahhoz, hogy az így kialakított életforma valóban üdvösségre vezet-e, szükség van az Egyház hitelesítésére. Akár a szerzetesi, akár bármely más közösségi vagy egyéni kezdeményezésről fontos tudnunk, hogy Isten elfogadja-e ezt az életáldozatot, elvezet-e ez a megszentelődésre. Ha kötött életszabály szerint, közösségben élem meg, akkor a legbiztosabb az egyházi jóváhagyás, ha egyénileg, akkor a gyóntató lelkiveze-tése.
            </li>
        </ol>
    </li>
</ol>
<ol start="3">
    <li>
        A Krisztusi élet jegyei
        <ol>
            <li>
                Ami az odaadó keresztény élettől sokakat visszariaszt, az a kicsinyes, torzító kérdés: Miről kell lemondanom? Az igazi kérdés azonban: Mit nyerek vele, mi az igazi tartalma? A Krisztusi élet az a boldog haladás a kiteljesedés felé, amely bevon Isten világába.
            </li>
            <li>
                A kinyilatkoztatásnak elsőséget kell adnom az életemben! Nem élhetek úgy, mint aki biztosítást kötött Istennél, amit mellesleg néha el-elfelejt befizetni, de azért vissza-visszalopkodja a hitetlen világ ál-örömeit az életébe, pl. kapcsolatai, ízlésének deformálódása, stb. terén.
            </li>
            <li>
                Az imádság – együttélés, szeretet-kapcsolat Istennel, mégpedig az imádás kifejezésére, amelynek tartalma minden Istenhez fordulásban tudatosodik: Őt mindenkinél és mindennél jobban szeretem.
            </li>
        </ol>
    </li>
</ol>
