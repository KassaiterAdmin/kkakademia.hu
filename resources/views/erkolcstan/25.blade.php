                Helyezzem bele a reggeli készületembe az ima konkrét perceit, melyekben megújítom az elhatározásomat. A kora délutáni órákban jelez a telefonom: hol tartok eddig?
            </li>
            <li>
                Jelöljem ki az esti lelkiismeretvizsgálat konkrét idejét! Ennek végén ne maradjon el a kérdés: hogyan állok az elhatározásommal? Hogy sikerült? Mikor nem és miért nem? Mikor igen és ezt mi segítette? Mit eredményezett a lelki fejlődésemben?
