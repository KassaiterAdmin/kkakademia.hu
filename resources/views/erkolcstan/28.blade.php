<p>
    <strong>
        A feltámadott élet – fontossági sorrend
    </strong>
</p>
<p>
    A keresztény élet konkrét értékrenden alapul. A külső tettek és a belső világ összhangja kettős jelentésű:
</p>
<ul>
    <li>
        a külső cselekedetekre helyezett hangsúly elősegítheti a helytelen, esetleg képmutató indítékokat, ezért nem lehet egyetlen mércéje az érdemszerző életnek,
    </li>
    <li>
        a csak belső indíttatáson alapuló élet konkrét tettek nélkül viszont túl elvont, tanúság nélküli.
    </li>
</ul>
<p>
    Nem a tettek és kezdeményezések, nem a nagy lelki fellángolások számítanak, hanem a feltámadott életre törekvő hívő ember személyiségének összhangja. Ezt látjuk a szentek életében is. Ezért fontos a helyes értékrendre épülő személyiség.
</p>
<p>
    A fontossági sorrend:
    <strong>
        Isten – kötelesség – többi ember – önmagam
    </strong>
</p>
<ol>
    <li>
        Isten
        <ol>
            <li>
                Ha a mindenség Ura belép az életembe, akkor kettős módon fogadhatom be:   
                <ul>
                    <li>
                        megadom a neki kijáró közvetlen tiszteletet, erre időt, alkalmat biztosítok, ezért másról lemondó áldozatot hozok.
                    </li>
                    <li>
                        az Ő szolgálata szerint teszek minden mást is  – Övé az elsőség mindenben.
                    </li>
                </ul>
            </li>
            <li>
                Az imádságnak konkrét helyet szentelek a napirendemben: időpont, időtartam, alkalom, légkör szerint.
            </li>
            <li>
                A szentmise igazi áldozat számomra - nemcsak időben és elfoglaltságban, de jellegben, lelkületben is eköré csoportosul minden, méghozzá egész héten. Ez szentel meg minden nemes kezdeményezést az életemben.
            </li>
        </ol>
    </li>
</ol>
<ol start="2">
    <li>
        A kötelesség: a küldetés kibontakozása – elsősorban Istennek tartozom elszámolni róla.
        <ol>
            <li>
                Nem kell, hogy kedvem teljék benne, elég, hogy a helyemet megtaláljam általa, és ez tölt el benső békével.
            </li>
            <li>
                Nem az a kérdés, hogy milyen kötelesség kedvező nekem, hanem hogy miben vagyok fontos a világ számára.
            </li>
        </ol>
    </li>
    <li>
         A többi ember a kapcsolatok helyes hierarchiája alapján vesz körül:
        <ol>
            <li>
                akikhez Isten küldött, akik közé helyezett (családi felelősségi kör),
            </li>
            <li>
                az egyház (testvéri kör),
            </li>
            <li>
                a nemzet (identitás-kör),
            </li>
            <li>
                a szerelem (küldetés-keresés),
            </li>
            <li>
                a barátság (kölcsönös támasz helyünk keresésében),
            </li>
            <li>
                a felebarát ( a szeretet készsége váratlan helyzetekben),
            </li>
            <li>
                az ellenség ( a szeretet jézusi egyetemessége). 
            </li>
        </ol>
    </li>
</ol>
<ol start="4">
    <li>
        Önmagam
        <ol>
            <li>
                Az érvényesülés (ez tehát nem a kötelesség témaköre) segít érvényre juttatni tehetségemet, kötelességeim körét, kedvet jelent a szolgálathoz,
            </li>
            <li>
                a pihenés (erőgyűjtés),
            </li>
            <li>
                a szórakozás, kikapcsolódás (mindannak frissítése, amiben fontos vagyok – magamra és másokra találás saját, helyes természetünk szerint).
            </li>
        </ol>
    </li>
</ol>
<p>
    Beszéljük meg:
</p>
<p>
    Szerinted ez a fenti, helyes sorrend
</p>
<ol>
    <li>
        hogyan és
    </li>
    <li>
        miért bomlik fel és rendeződik át általában
    </li>
    <li>
        a mai és
    </li>
    <li>
        a mai vallásos fiatalok életében?
    </li>
</ol>