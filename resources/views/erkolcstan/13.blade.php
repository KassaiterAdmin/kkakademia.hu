<p>
    Mit jelent a profán, konkrét tartalom nélküli megfogalmazás, hogy a Karácsony a szeretet ünnepe? Nekünk, keresztényeknek nagyon találó: úgy szeretett minket, embereket Isten, hogy emberré lett, ezzel emberi természetben megmutatta az istenszeretet lehetséges távlatait, hogy ezután az embert is, a többieket és önmagunkat is az Ő szemlélete szerint szeressük.
</p>
<ol>
    <li>
        Kinek tartanak engem az emberek? – kérdezi Jézus tanítványaitól közvetlen az előtt, mielőtt Pétert az egyház tévedhetetlen alapjává teszi.
        <ol>
            <li>
                Az arianizmus 4. századi eretnekség, amely szerint Jézus csak ember, igaz, a legkiválóbb, de nem volt isteni természete.
            </li>
            <li>
                A nesztorianizmus az 5. századi, félreértett tanításként tagadja Jézus személyi egységét.
            </li>
            <li>
                A monofizitizmus 5. század végi tévtanítás, amely szerint Jézus emberi természete fel oldódott az isteni természetben, ezzel egy természetté vált: Ő csak Isten (a tévedés itt is a túlzó hitvédelmi fogalmazásból eredt).
            </li>
            <li>
                Az efféle hitbeli tévedéseket követők attól a felelősségtől szabadítanák meg a keresztényeket, amely hangsúlyozza, hogy az ember rendeltetése, hogy az isteni életet befogadja.
            </li>
        </ol>
    </li>
</ol>
<ol start="2">
    <li>
        Hát ti kinek tartotok engem? – kérdezi Jézus, miután végighallgatta saját korának tévedéseit.
        <ol>
            <li>
                Az én Krisztusom: az Egyház hiteles és teljes Krisztusa. Akit szeretek, azt ismernem is kell,  szenvedélyesen érdeklődve a róla szóló teljes igazság iránt.
            </li>
            <li>
                Krisztus jelenléte az életemben azt jelenti, hogy meghívást kaptam a Szentháromság életébe, az egyetlen tökéletes szeretet-életbe.
            </li>
            <li>
                Az Istenemberbe vetett tiszta hit pedig visszahat saját emberi méltóságomra is, így kell számon tartanom önmagam, erre épülnek fel tetteim, érzésvilágom.
            </li>
        </ol>
    </li>
</ol>
<ol start="3">
    <li>
        Történelmi körülmények és összefüggések
        <ol>
            <li>
                Jézus az Ószövetségi ígéretek beteljesítője, megtestesülésével, életével, tanításával a kinyilatkoztatást teszi teljessé.
            </li>
            <li>
                Vállalja az egy nemzethez tartozást, de egyetemessé teszi az Isten és ember közötti örök szövetséget.
            </li>
            <li>
                Sajátos körülmények: megszállt területre születik, melyen egyszerre van jelen a birodalmi kozmopolitizmus és a zsidó nacionalizmus, ami vallási köntösben jelenik meg. Egyszerre él népében az elnyomottság-érzet és a faji, vallási kirekesztés a szamaritánusokkal és a pogányokkal szemben.
            </li>
            <li>
                Jézus egy minden korra vonatkozó életeszményt ad: nem az osztályharcos kereszténységet, hanem magával a bűnnel felvett küzdelem követelményét.
            </li>
            <li>
                Az egyházban fordított társadalmi képletet állít fel:
                <em>
                    „Aki közületek nagyobb  
                </em>
                <em>
                    akar lenni, legyen a szolgátok, s aki első akar lenni, legyen a
                </em>
                <em>
                    cselédetek
                </em>
                ”(Máté evangéliuma 20, 26-27).
            </li>
        </ol>
    </li>
</ol>
<ol start="4">
    <li>
        Az Egyházhoz tartozás: a hit engedelmességén keresztül
        <ol>
            <li>
                 Tehát: a teljes Krisztust vallom, nemcsak elfogadom, de érdekel is, eszerint alakul Vele személyes viszonyom.
            </li>
            <li>
                A karácsonyi pillanat átélése: ÉRTEM JÖTT, tehát nem általában az emberiségért, hanem értem, az emberért. Ha egy természeti világkatasztrófa után egyedül éltem volna a földön, akkor is eljött volna értem, hogy megváltson. Ez nem statisztikai kérdés, ez a személyes isteni szeretet műve.
            </li>
            <li>
                Az erre adott személyes válaszom, a szívből megfogalmazott ima az Ő jászolánál: érted élek, és Benned másokért.
            </li>
        </ol>
    </li>
</ol>
