<p>
    <strong>
        Nagyhétre készülök – személyes jelenlétem a liturgiában
    </strong>
</p>
<p>
     
</p>
<p>
    A liturgia a Szentlélek művészete: Jézus jelképes, de érzékelhető, misztikus, de valóságos élete köztünk, a mi szolgálatunk révén. A szent idők, színek, személyek, szerepek, igék, cselekmények Krisztus közelségét fejezik ki a Titokzatos Testben, ahogyan a csontok, idegek, izmok, bőr, vér, stb. – gesztusok, arcvonások, a természetes emberi közelség megjelenítői.
</p>
<p>
     
</p>
<ol>
    <li>
        Ahogy a liturgiára készülök
        <ol>
            <li>
                Ezek az alkalmak részvételt jelentenek Krisztus életének eseményeiben: ma ott leszek az utolsó vacsorán, a kereszt alatt, stb.
            </li>
            <li>
                Az evangéliumi szemlélődés azt jelenti, hogy elmélkedésem tárgya, a képzelet, sőt átélés, hogy ott vagyok, hiszen a liturgiában testileg és érzékeimmel is jelen vagyok.
            </li>
            <li>
                A jelképeken a lelkem tör át: ezért kell a benső készület, nem csupán elkülönített gondolatok, hanem a liturgiáig vezető eseményeket már igyekszem aszerint a szemlélet szerint feldolgozni, amit napokon, órákon belül átélek.
                <em>
                    Más színben élem meg a világot, ha szerelmes vagyok, ha gyászolok, ha életveszélyben vagyok, ha a feladatok új távlata nyílik meg előttem.
                </em>
                Nagyhétre készülve aszerint dolgozom fel napjaimat, hogy Jézus szenvedéstörténetére készülök. Itt tárul fel a világ igazi arculata.
            </li>
        </ol>
    </li>
</ol>
<p>
     
</p>
