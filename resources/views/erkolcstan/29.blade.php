<p>
    <strong>
        „Megnyitotta értelmüket az írásokra…” Személyes életem Isten szava mellett
    </strong>
</p>
<p>
    <em>
        Izajás próféta könyvének 55. fejezetében olvassuk:”Amint az eső és a hó lehull az égből, és nem tér oda vissza, hanem megöntözi a földet, és termővé, gyümölcsözővé teszi, hogy magot adjon a magvetőnek és kenyeret az éhezőnek, éppen úgy lesz a szavammal is, amely ajkamról fakad. Nem tér vissza hozzám eredménytelenül, hanem végbeviszi akaratomat, és eléri, amiért küldtem”(10-15).
    </em>
</p>
<ol>
    <li>
        Isten Igéje
        <ol>
            <li>
                „Elvégzi feladatát”: felkínálkozik, kegyelmet közvetít, Istennel találkoztat.
            </li>
            <li>
                Jézus sorsát viseli, minthogy Ő Isten örök Igéje: „jel, amelynek ellentmondanak”, „a világosság a világba jött, de a sötétség nem fogta fel”
            </li>
            <li>
                Az elutasítók, akik mellett elmegy az élet lehetősége – ítéletet vonnak magukra, mert
                <ul>
                    <li>
                         Isten számon kéri az elutasítást, és a közönyt,
                    </li>
                    <li>
                        „Magukra vonnak ítéletet”, mert ez lenne számukra az egyetlen menekülési lehetőség.
                    </li>
                    <li>
                         Megfigyelhető, ahogy a nyár folyamán a különféle műemlék templomokba különféle célokkal térnek be az emberek. Van, aki pontosan tudja, ki várja bent, imádkozik, de azért szét is néz, meg is csodálja a szépségeket, van pedig, aki csak bámészkodik, semmibe véve az isteni jelenlétet. Az ilyen ember elsuhan egy nagy lehetőség, az Istennel való találkozás esélye mellett.
                    </li>
                    <li>
                        Az Isten igéjéhez még a templomba sem kell bemennünk, karnyújtásnyira van tőlünk, a polcunkon, a számítógépünkön.
                    </li>
                </ul>
            </li>
            <li>
                Gyakorlati megnyilvánulások
                <ol>
                    <li>
                        A szentmisében hallott olvasmány, szentlecke ne suhanjon el a fülem mellett! Legalább egy-egy gondolatot merítsek belőlük, esetleg nézzem meg előre őket!
                    </li>
                    <li>
                        A napi evangélium jöhet e-mailban ez lehet a legrendszeresebb mindennapi ige-élet. Csak azt olvassam el, amiről elmélkedek is, szánjak erre egy gondolatnyi, elhatározásnyi időt!
                    </li>
                </ol>
            </li>
        </ol>
    </li>
    <li>
        „Megnyitotta értelmüket…”
        <ol>
            <li>
                Lukács evangéliumának 24. fejezetében olvassuk, amint Jézus  így szólt tanítványaihoz: „Ezeket mondtam nektek, amikor még veletek voltam, hogy be kell teljesednie mindannak, amit rólam Mózes törvényében, a prófétákban és a zsoltárokban írtak. Ekkor megnyitotta értelmüket, hogy megértsék az Írásokat, s így folytatta: Meg van írva, hogy a Messiásnak szenvednie kell, és harmadnap fel kell támadnia a halálból. Nevében megtérést és bűnbocsánatot kell hirdetni minden népnek Jeruzsálemtől kezdve. Ti tanúi vagytok ezeknek. Én meg kiárasztom rátok Atyám ígéretét. Maradjatok a városban, míg fel nem öltitek a magasságból való erőt. Ezután kivezette őket Betánia közelébe, és kezét kitárva megáldotta őket. Áldás közben megvált tőlük, és fölemelkedett az égbe. Leborulva imádták, aztán nagy örömmel visszatértek Jeruzsálembe. Mindig a templomban voltak, és dicsőítették az Istent”(44-53).
            </li>
            <li>
                Az értelem megnyitása:
                <ul>
                    <li>
                        az Írásból kiemelem a súlypontokat, fontosság szerint, ezek az élet bölcsességének alappillérei,
                    </li>
                    <li>
                        a kinyilatkoztatásban megnyilvánuló harmónia, hogy a józan élettapasztalat igazolja az Írást, míg az Írás a helyes életszemléletet, amelyre megint csak az élettapasztalat tesz pecsétet: tehát élet és Írás kölcsönösen igazolják egymást.
                    </li>
                    <li>
                        A Jézus által megnyitott értelem nem egyszerű logikai okfejtés, hanem maga a kinyilatkoztató adja a kegyelmet a megértéshez is – az imádság légkörében.
                    </li>
                </ul>
            </li>
        </ol>
    </li>
    <li>
        Lelki feldolgozás
        <ol>
            <li>
                A bibliaolvasás mindig konkrét elhatározással, cselekvési programmal végződjön: mi velem Isten akarata? Az erről való elmélkedésben
                <ul>
                    <li>
                        nyissam fel életemben a fájó, sántító, hátráltató gócpontokat,
                    </li>
                    <li>
                        keressek rájuk megoldási kísérleteket az Úr tanácsai szerint.
                    </li>
                </ul>
            </li>
            <li>
                Semmilyen elmélkedés nem ad új igazságot: az Egyház tanítása alapvető.
            </li>
            <li>
                Élettapasztalatom tovább érlelődik a Biblia tanításának tükrében,
                <ul>
                    <li>
                        az alapvető bölcsességek alapján, ahogyan levonom egy-egy esemény tanulságait az örök távlatokban.
                    </li>
                    <li>
                        A Lélek általában a válságban mutatja meg igazi erejét, mint az első Pünkösdkor, amíg a tanítványok nem tudtak mit kezdeni Jézus missziós parancsával. A Lélek szerepe az életemben: Őelé tárok mindent, ami bennem még válságos.
                    </li>
                </ul>
            </li>
        </ol>
    </li>
</ol>
