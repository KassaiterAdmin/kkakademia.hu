        Elfogadsz egy zsákbamacska lehetőséget, és már nincs visszaút. Ez áll benne: Vagy átalszol egy teljes napot, és ez egyszerűen kimarad az életedből, vagy kapsz egy lehetőséget, hogy egy másik ember testében ébredj és egy napot abban tölts. Választhatsz tulajdonságaival körülírt, vagy konkrét személyt.
    </li>
    <li>
        Van-e kedvenc bibliai idézeted, elmondhatod-e, hogy mikor, miben jelentett komoly kapaszkodót számodra, vagy egyáltalán hogyan került kiemelten a látóteredbe?
    </li>
</ol>